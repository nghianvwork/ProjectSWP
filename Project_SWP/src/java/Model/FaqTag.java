package Model;

public class FaqTag {

    private int tagId;
    private String name;

    public FaqTag() {
    }

    public FaqTag(int tagId, String name) {
        this.tagId = tagId;
        this.name = name;
    }

    public int getTagId() {
        return tagId;
    }

    public void setTagId(int tagId) {
        this.tagId = tagId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "FaqTag{" + "tagId=" + tagId + ", name=" + name + '}';
    }

}
